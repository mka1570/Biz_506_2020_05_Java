package com.biz.hello;

public class Var_Add_01 {

		public static void main(String[] args) {
			
			int num1 = 40;
			int num2 = 30;
			
			System.out.println(num1 + " + " + num2 +" = " + (num1 + num2));
			System.out.println(num1 + " - " + num2 +" = " + (num1 - num2));
			System.out.println(num1 + " / " + num2 +" = " + (num1 / num2));
			System.out.println(num1 + " * " + num2 +" = " + (num1 * num2));
			
						
		}
}
